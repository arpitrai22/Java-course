public class relationaloperators {
  
   public static void main(String[] args) {

    int a = 10;
    int b = 20;

    //Relational operators
    System.out.println("is a = b: " + (a == b));  //false
    System.out.println("is a!= b: " + (a != b)); //True
    System.out.println(" is a > b: " + ( a > b ) );  //false
    System.out.println("is a < b: " + ( a < b ) );  //True
    System.out.println(" is a >= b: " + ( a >= b ) );  //false
    System.out.println("is a <= b: " + ( a <= b ) );  //True


    
   }
}
